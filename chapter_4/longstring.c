// 打印较长字符串的三种方法
#include <stdio.h>

int main(void)
{
    printf("Here's one way to print a ");
    printf("long string.\n"); // 第一种方法

    printf("Here's another way to print a \
long string.\n"); // 第二种方法

    printf("Here's the newest way to print a "
           "long string.\n"); // 第三种方法

    return 0;
}